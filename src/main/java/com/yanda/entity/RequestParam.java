package com.yanda.entity;

public class RequestParam {
	/**
	 * 参数名
	 */
	private String name;
	
	/**
	 * 参数值
	 */
	private String value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
}
